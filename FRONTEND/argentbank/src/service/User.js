class User {
    constructor(createdAt, email, firstname, id, lastname, updatedAt, username) {
        this.createdAt = createdAt;
        this.email = email;
        this.firstname = firstname;
        this.id = id;
        this.lastname = lastname;
        this.updatedAt = updatedAt;
        this.username = username;
    }
}

export default User;